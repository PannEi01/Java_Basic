public class Arithmetic_operators {
    public static void main(String[] args) {
        int num1 = 30;
        int num2 = 23;
        int result = num1 + num2;

        System.out.println(result);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
    }
}
